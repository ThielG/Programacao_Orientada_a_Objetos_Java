
package aula0304;

public class Calculadora {
    
    public int somar(int primeiro_numero, int segundo_numero){
    return primeiro_numero + segundo_numero;
    }
    
    public int subtrair(int primeiro_numero, int segundo_numero){
    return primeiro_numero - segundo_numero;
    }
    
    public int multiplicar(int primeiro_numero, int segundo_numero){
    return primeiro_numero * segundo_numero;
    }

}
