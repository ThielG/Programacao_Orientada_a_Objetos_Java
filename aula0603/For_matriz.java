package poo2;

public class For_matriz {
    
    public static void main(String[] args){
        
        for(int linha = 0; linha < 4; linha ++){
            for(int coluna = 0; coluna < 4; coluna++){
                System.out.print(linha + "" + coluna + " ");
            }
            
            System.out.println("");
            
        }
        
    }
    
}
