
package poo2;

public class For {
    
    public static void main(String[] args){
    
        for(int contador = 1; contador <= 10 ; contador++){
            
            System.out.println(contador);
            
            if(contador == 5){
                System.out.println("Acabou o laço de repetição.");
                break;
                
            }
            
        }

    } 
    
}

